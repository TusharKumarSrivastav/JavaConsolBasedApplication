package com.fossil.carrent.util;
/**
 * This class is encapsulated class.
 * @author Tushar
 *
 */
public class EncapsulatedClass {
  private String vehiclecategory;
  private  String fueltype;
  private String registrationnumber;
  private float milage;
  private  float dailyrent;
  /**
   * This is getter method of VehicleCategory
   * @return VehicleCategory
   */
public String getVehiclecategory() {
	return vehiclecategory;
}
/**
 * This is setter method of VehicleCategory
 * @param vehiclecategory
 */
public void setVehiclecategory(String vehiclecategory) {
	this.vehiclecategory = vehiclecategory;
}
/**
 * This is getter method of FuelType
 * @return fueltype
 */
public String getFueltype() {
	return fueltype;
}
/**
 * This is setter method of FuelType
 * @param fueltype
 */
public void setFueltype(String fueltype) {
	this.fueltype = fueltype;
}
/**
 * This is getter method of registrationnumber
 * @return registrationnumber
 */
public String getRegistrationnumber() {
	return registrationnumber;
}
/**
 * This is setter method of registrationnumber
 * @param registrationnumber
 */
public void setRegistrationnumber(String registrationnumber) {
	this.registrationnumber = registrationnumber;
}
/**
 * This is getter method of milage
 * @return milage
 */
public float getMilage() {
	return milage;
}
/**
 * This is setter method of milage
 * @param milage
 */
public void setMilage(float milage) {
	this.milage = milage;
}
/**
 * This is getter method of dailyrent
 * @return dailyrent
 */
public float getDailyrent() {
	return dailyrent;
}
/**
 * This is setter method of dailyrent
 * @param dailyrent
 */
public void setDailyrent(float dailyrent) {
	this.dailyrent = dailyrent;
}
  
}
